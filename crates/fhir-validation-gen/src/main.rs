mod emit;
mod model;
mod versions;

use std::env;
use std::fs;
use std::path::{Path, PathBuf};

use extract::extract_type_validation_models;
use emit::emit_types;
use helios_fhir_gen::initial_fhir_model::StructureDefinition;
use serde_json::Value;
use versions::FhirVersion;

fn main() {
    if let Err(err) = run() {
        eprintln!("fhir-validation-gen: {err}");
        std::process::exit(1);
    }
}

fn run() -> Result<(), String> {
    let args: Vec<String> = env::args().collect();

    if args.len() < 5 {
        return Err(usage(&args));
    }

    let version: FhirVersion = args[1]
        .parse()
        .map_err(|e: String| format!("invalid FHIR version '{}': {e}", args[1]))?;

    let output_path = PathBuf::from(args.last().expect("output path missing"));
    let input_paths: Vec<PathBuf> = args[2..args.len() - 1]
        .iter()
        .map(PathBuf::from)
        .collect();

    let mut all_defs = Vec::new();
    for input_path in &input_paths {
        let defs = load_structure_definitions_from_bundle(input_path)?;
        all_defs.extend(defs);
    }

    let mut models = Vec::new();
    for def in &all_defs {
        let extracted = extract_type_validation_models(version, def).ok_or_else(|| {
            "could not extract validation models from one of the input bundles".to_string()
        })?;
        models.extend(extracted);
    }

    let mut output = String::new();
    emit_file_prelude(version, &input_paths, all_defs.len(), &mut output);
    emit_types(version, &models, &mut output);

    write_output(&output_path, &output)?;

    println!(
        "Generated validation code for {} type(s) from {} bundle(s) ({}) -> {}",
        models.len(),
        input_paths.len(),
        version,
        output_path.display()
    );

    Ok(())
}

fn usage(args: &[String]) -> String {
    let bin = args
        .first()
        .map(String::as_str)
        .unwrap_or("fhir-validation-gen");

    format!(
        "usage: {bin} <r4|r4b|r5|r6> <input-bundle-1.json> [<input-bundle-2.json> ...] <output.rs>"
    )
}

fn load_structure_definitions_from_bundle(path: &Path) -> Result<Vec<StructureDefinition>, String> {
    let text = fs::read_to_string(path)
        .map_err(|e| format!("failed to read '{}': {e}", path.display()))?;

    let json: Value = serde_json::from_str(&text)
        .map_err(|e| format!("failed to parse JSON '{}': {e}", path.display()))?;

    let resource_type = json
        .get("resourceType")
        .and_then(Value::as_str)
        .ok_or_else(|| format!("missing resourceType in '{}'", path.display()))?;

    if resource_type != "Bundle" {
        return Err(format!(
            "expected Bundle input in '{}', found resourceType='{}'",
            path.display(),
            resource_type
        ));
    }

    let entries = json
        .get("entry")
        .and_then(Value::as_array)
        .ok_or_else(|| format!("bundle '{}' is missing entry[]", path.display()))?;

    let mut defs = Vec::new();

    for entry in entries {
        let Some(resource) = entry.get("resource") else {
            continue;
        };

        let Some(resource_type) = resource.get("resourceType").and_then(Value::as_str) else {
            continue;
        };

        if resource_type != "StructureDefinition" {
            continue;
        }

        let def: StructureDefinition = serde_json::from_value(resource.clone()).map_err(|e| {
            format!(
                "failed to parse StructureDefinition entry in '{}': {e}",
                path.display()
            )
        })?;

        defs.push(def);
    }

    if defs.is_empty() {
        return Err(format!(
            "bundle '{}' did not contain any StructureDefinition entries",
            path.display()
        ));
    }

    Ok(defs)
}

fn write_output(path: &Path, contents: &str) -> Result<(), String> {
    if let Some(parent) = path.parent() {
        if !parent.as_os_str().is_empty() {
            fs::create_dir_all(parent).map_err(|e| {
                format!(
                    "failed to create output directory '{}': {e}",
                    parent.display()
                )
            })?;
        }
    }

    fs::write(path, contents)
        .map_err(|e| format!("failed to write '{}': {e}", path.display()))
}

fn emit_file_prelude(
    version: FhirVersion,
    input_paths: &[PathBuf],
    structure_definition_count: usize,
    output: &mut String,
) {
    output.push_str("// @generated by fhir-validation-gen\n");
    output.push_str(&format!("// FHIR version: {}\n", version));
    output.push_str("// Source StructureDefinition bundles:\n");
    for input_path in input_paths {
        output.push_str(&format!("//   - {}\n", input_path.display()));
    }
    output.push_str(&format!(
        "// StructureDefinitions processed: {}\n\n",
        structure_definition_count
    ));
}
